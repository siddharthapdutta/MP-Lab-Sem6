ASSUME CS:CODE, DS:DATA

DATA SEGMENT
	ORG 1000h
	NUM1 DW 9AFFH, 2BCDH
	NUM2 DW 9EFEH, 5EFFH
	RES DW 2 DUP(0)
DATA ENDS

CODE SEGMENT
START:	MOV AX, DATA
		MOV DS, AX

		MOV AX, NUM1
		MOV BX, NUM2
		ADD AX, BX
		MOV RES, AX

		MOV AX, NUM1+2
		MOV BX, NUM2+2
		ADC AX, BX
		MOV RES+2, AX
		MOV AH, 4Ch
		INT 21h
CODE ENDS
END START