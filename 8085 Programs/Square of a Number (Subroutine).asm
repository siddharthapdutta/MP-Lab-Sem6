// Square of a Number using Subroutine
// Number is Stored in 3000H
# ORG 3000H
# DB FF

# ORG 0000H
LDA 3000H
MOV C,A
PUSH B
CALL MUL
POP H
SHLD 2000H
HLT

MUL: POP H
SHLD 5000H
POP B
MOV D,C
LXI H, 0000H
Add: DAD B
DCR D
JNZ Add
PUSH H
LHLD 5000H
PUSH H
RET

// Result is Stored in 2000H and 2001H
